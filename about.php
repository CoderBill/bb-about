		<li>Agreeing to keep your business practices to the highest ethical and professional standards of your industry.</li>
		<li class="small">Further agreeing to conduct yourself as an ambassador of BizBuilders in a respectfule, polite, and courteous manner.</li>
		<li class="small">Attending all meetings and assign a substitute to attend when you have a non-emergencey absence.</li>
	</ul>
