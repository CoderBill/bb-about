<div id="slide6" class="slide">
	<h2>Bylaws</h2>
	<p>Groups must qualify for chaptering in BizBuilders with the following requirements:</p>
	<ul>
		<li>Three consecutive weeks with more than 3 professionals in attendance.</li>
		<li>Three complteted membership applications with dues and business cards.</li>
		<li>The formal appointment of a chair and secretary.</li>
		<li>In order to maintain exclusivity and high quality, membership in BizBuilders Groups is subject to review and subsequent acceptance or denial by the group and Bizbuilders Corporate.</li>
		<li>Groups may establish membership committees to review applications and interviews of prospective members before presenting their findings to the whole group for a vote.</li>
		<li class="small">Members missing over three consecutive meetings can be replaced with someone else in that industry. Usually this is done by mutual consent.</li>
		<li class="small">Members are encouraged to commission a substitute if they know they will be absent.</li>
	</ul>
